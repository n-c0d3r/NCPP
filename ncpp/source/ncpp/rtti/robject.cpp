
#include <ncpp/.hpp>
#include <ncpp/rtti/robject.hpp>



namespace ncpp {

	namespace rtti {

		thread_local rcontext* current_context_p_g = 0;

		////////////////////////////////////////////////////////////////////////////////////
		////////////////////////////////////////////////////////////////////////////////////
		////////////////////////////////////////////////////////////////////////////////////

		reflect_base::reflect_base(rcontext& context) {

			current_context_p_g = &context;

		}

		////////////////////////////////////////////////////////////////////////////////////
		////////////////////////////////////////////////////////////////////////////////////
		////////////////////////////////////////////////////////////////////////////////////

		rcontext& current_context() {

			return *current_context_p_g;
		}

	}

}